class ejercicio21{
	public static void main(String[] args){
		System.out.println("------------- VARIABLES ENTERAS-----------------");
		
		int p = 100;
		int q = 101;
		System.out.println(p);
		System.out.println(q);
		
		System.out.println("-------------VARIABLES FLOAT-----------------");
		
		float x =  1.584f;
		float y = 1.92963f;
		float z = 12.34f;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		
		System.out.println("-------------VARIABLES CARACTER-----------------");
		
		char a = 'a';
		char b = 'b';
		char c = 'c';
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		System.out.println("-------------VARIABLES DOUBLE-----------------");
		
		double raiz1 = 34.6;
		double raiz2 = 24.354;
		System.out.println(raiz1);
		System.out.println(raiz2);
		
		System.out.println("------------VARIABLE ENTERA LARGA------------------");
		
		long contador = 1011121314151617L;
		System.out.println(contador);
		
		System.out.println("------------VARIABLE ENTERA CORTA------------------");
		
		short indicador = 617;
		System.out.println(indicador);
		
		System.out.println("------------VARIABLE BYTE------------------");
		
		byte valor = 11;
		System.out.println(valor);
		
		System.out.println("------------VARIABLES LOGICAS------------------");
				
		boolean primero = true;
		boolean ultimo = false;
		System.out.println(primero);
		System.out.println(ultimo);
				
		System.out.println("------------VARIABLE DE TIPO CADENA DE CARACTERES------------------");
		
		String nombre = "Marc Burguera";
		System.out.println(nombre);
		
	}
}